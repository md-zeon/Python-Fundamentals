# There are 4 types of Styles
# 1. Camel Case: In this style, the first letter of each word is capitalized, and there are no spaces or underscores between words. Example: MyVariableName, UserProfile, EmployeeData
# 2. Pascal Case: Similar to Camel Case, but the first letter of the first
#    word is also capitalized. Example: MyVariableName, UserProfile, EmployeeData
# 3. Snake Case: In this style, all letters are lowercase, and words are separated by underscores. Example: my_variable_name, user_profile, employee_data
# 4. Kebab Case: In this style, all letters are lowercase, and words are separated by hyphens. Example: my-variable-name, user-profile, employee-data
# In Python, the most commonly used style for variable names and function names is Snake Case. For example:
my_variable_name = "Hello, World!"
