# Data types in Python:
# 1. int (Integer) - Whole numbers without a decimal point. Example: 1, 42, -5
# 2. float (Floating-point) - Numbers with a decimal point. Example: 3.14, -0.001, 2.0
# 3. str (String) - A sequence of characters enclosed in quotes. Example: "Hello", 'World', "123"
# 4. bool (Boolean) - Represents True or False values. Example: True, False
# 5. list - An ordered collection of items. Example: [1, 2, 3], ["apple", "banana", "cherry"]
# 6. tuple - An ordered, immutable collection of items. Example: (1, 2, 3), ("apple", "banana", "cherry")
# 7. dict (Dictionary) - A collection of key-value pairs. Example: {"name": "Alice", "age": 30}, {"fruit": "apple", "color": "red"}
# 8. set - An unordered collection of unique items. Example: {1, 2, 3}, {"apple", "banana", "cherry"}
# 9. NoneType - Represents the absence of a value. Example: None
# Example of using different data types:
my_height = 5.7  # float
is_student = True  # bool
hobbies = ["reading", "swimming", "coding"]  # list
address = ("123 Main St", "City", "Country")  # tuple
person_info = {"name": "Zeon", "age": 22}  # dict
unique_numbers = {1, 2, 3, 4, 5}  # set
my_value = None  # NoneType
print(type(my_height))
print(type(is_student))
print(type(hobbies))
print(type(address))
print(type(person_info))
print(type(unique_numbers))
print(type(my_value))
